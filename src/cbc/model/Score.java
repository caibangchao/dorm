package cbc.model;

public class Score {

	private String id;
	private String dorm_id;
	private String neat;
	private String clear;
	private String smell;
	private String discipline;
	private String score_sum;
    private String update_date;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getDorm_id() {
		return dorm_id;
	}
	public void setDorm_id(String dorm_id) {
		this.dorm_id = dorm_id;
	}
	public String getNeat() {
		return neat;
	}
	public void setNeat(String neat) {
		this.neat = neat;
	}
	public String getClear() {
		return clear;
	}
	public void setClear(String clear) {
		this.clear = clear;
	}
	public String getSmell() {
		return smell;
	}
	public void setSmell(String smell) {
		this.smell = smell;
	}
	public String getDiscipline() {
		return discipline;
	}
	public void setDiscipline(String discipline) {
		this.discipline = discipline;
	}
	public String getScore_sum() {
		return score_sum;
	}
	public void setScore_sum(String score_sum) {
		this.score_sum = score_sum;
	}
	public String getUpdate_date() {
		return update_date;
	}
	public void setUpdate_date(String update_date) {
		this.update_date = update_date;
	}

	
	
}
